'''
Polymorphism
============

poly     => Many
morphism => Forms

A person plays different or many [poly] roles [forms]in his/her
life. This is called as polymorphism in real life.
'''

a=10
b=20
c=a+b #Here '+' is arithmatic operator.
print("Addition is:",c)

x="Hello"
y="World"

z=x+y #Here '+' is concatination operator.
print("Value of z is:",z)


'''
In above program operators + is used as arithmetic operator
when operands are integer, + is used as concatenation operator
when operands are string.
Thus single operator playing two different role,i.e arithemetic
and concatenation operator based on operands.

Single operator + has two forms. This is called as operator
overloading which is means to achieve polymorphism.

'''
