'''
User defined function
=====================

Syntax:

def function_name(arguments):

         body of function

         return value

Function call:
==============

function_name()


When a function call is detected. Program control goes to
function body,it execute function body and after executing
function body control returns at the place of the
function call.


'''


def greet():

   print("Hello all")
   print("Good evening")

greet()     #calling of function



